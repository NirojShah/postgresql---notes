-- Order By is used for the sorting.

-- Accending order
SELECT * from users2 ORDER BY date_of_birth ASC;

SELECT * from users2 ORDER BY first_name ASC;


SELECT * from users2 ORDER BY id ASC;



-- Decending Order.

SELECT * from users2 ORDER BY date_of_birth DESC;


SELECT * from users2 ORDER BY first_name DESC;


SELECT * from users2 ORDER BY id DESC;



